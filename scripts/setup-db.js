async function main() {
  // 1. Add enum values (idempotent via IF NOT EXISTS)
  await prisma.$executeRawUnsafe(
    `ALTER TYPE "TipoInstitucion" ADD VALUE IF NOT EXISTS 'RURAL_URBANA'`
  );
  await prisma.$executeRawUnsafe(
    `ALTER TYPE "TipoInstitucion" ADD VALUE IF NOT EXISTS 'URBANA_RURAL'`
  );
  console.log('Enum values: OK');

  // 2. Change Municipio unique: drop old single-column index, create composite index
  // Prisma creates @unique as a UNIQUE INDEX (not a constraint), so DROP INDEX not DROP CONSTRAINT
  await prisma.$executeRawUnsafe(`DROP INDEX IF EXISTS "Municipio_nombre_key"`);
  await prisma.$executeRawUnsafe(`
    CREATE UNIQUE INDEX IF NOT EXISTS "Municipio_nombre_tipoUso_key"
    ON "Municipio" ("nombre", "tipoUso")
  `);
  console.log('Municipio unique constraint: OK');

  // 3. Assign zones using a single bulk UPDATE with a VALUES table
  const valuesClause = ZONE_DATA
    .map(([n, m, z]) => `('${n}', '${m}', '${z}')`)
    .join(',\n    ');

  const sql = `
    UPDATE "Institucion" i
    SET "tipoInstitucion" = vals.zona::"TipoInstitucion"
    FROM (VALUES
      ${valuesClause}
    ) AS vals(nombre, municipio, zona)
    WHERE i.nombre = vals.nombre
      AND i."municipioId" IN (SELECT id FROM "Municipio" WHERE nombre = vals.municipio)
  `;

  const result = await prisma.$executeRawUnsafe(sql);
  console.log(`Zones assigned: ${result} rows updated`);
}

main()
  .catch(e => { console.error('setup-db error:', e.message); process.exit(1); })
  .finally(() => prisma.$disconnect());
